"""
Codo CLI 入口点 - 快速路径分发器

这个模块负责：
1. 检查特殊命令标志（如 --version）并提供快速路径
2. 对于默认路径，动态导入完整 CLI 并启动

[Workflow]
1. 解析命令行参数
2. 检查 --version 快速路径（零模块加载）
3. 默认路径：动态导入 main.py 并启动完整 CLI
"""

import sys  # 用于访问命令行参数
import asyncio  # 用于异步执行

# 版本号
# 在构建时应该被替换为实际版本号
VERSION = "0.1.0"

def main() -> None:
    """
    CLI 主入口函数，负责快速路径分发。

    [Workflow]
    1. 获取命令行参数（去除程序名）
    2. 检查 --version/-v/-V 快速路径
    3. 默认路径：加载完整 CLI
    """
    # 获取命令行参数，去除第一个元素（程序名）
    # sys.argv[0] 是 'python -m codo' 或 'codo'
    # sys.argv[1:] 是实际的用户参数
    args = sys.argv[1:]

    # ============================================================
    # 快速路径 1: --version / -v / -V
    # 零模块加载，直接输出版本号并退出

    # ============================================================
    if len(args) == 1 and args[0] in ("--version", "-v", "-V"):
        # 直接打印版本号，格式：版本号 (Codo)

        print(f"{VERSION} (Codo)")
        return  # 直接返回，不加载任何其他模块

    # ============================================================
    # 默认路径：加载完整 CLI

    # ============================================================

    # 动态导入 main 模块，避免在快速路径中加载所有依赖
    # 这样 --version 命令可以在毫秒级响应
    from codo.main import main as full_cli_main

    # 调用完整 CLI 的 main() 函数（同步函数）
    # 这会启动 click 命令解析器和 REPL
    full_cli_main()

# 如果直接运行此文件（不太可能，因为应该通过 __main__.py 启动）
if __name__ == "__main__":
    # 直接调用同步 main 函数
    main()
